"""Core agent logic shared by both memory and no-memory modes.

Key design decisions:
- The sales agent does NOT see the customer's persona tag — only their message.
- Customer responses are generated by an LLM (for natural hints) but outcomes are
  determined by DETERMINISTIC rules based on what feature was pitched.
- This eliminates LLM randomness from win/loss decisions while keeping realistic dialogue.
"""

from __future__ import annotations

import json
from typing import Awaitable, Callable, Optional

import cognee
from cognee.infrastructure.llm.LLMGateway import LLMGateway
from cognee.modules.engine.operations.setup import setup

from .catalog import FEATURE_DESCRIPTIONS, CogneeFeature
from .leads import BuyingProfile, LEADS
from .models import ConversationResult, CustomerHint, SalesResponse

MAX_ROUNDS = 2

# Sales function signature: (history, lead_intro, round_num, memory_context) -> SalesResponse
SalesFn = Callable[[list, str, int, str], Awaitable[SalesResponse]]


def _feature_catalog_text() -> str:
    return json.dumps(
        {f.value: d["tagline"] for f, d in FEATURE_DESCRIPTIONS.items()},
        indent=2,
    )


async def sales_agent_turn(
    conversation_history: list[dict],
    lead_intro: str,
    round_num: int,
    memory_context: str = "",
) -> SalesResponse:
    """Generate one sales pitch turn. The agent does NOT know the customer's persona."""
    memory_block = ""
    if memory_context:
        memory_block = (
            f"PAST SALES LEARNINGS (use these to guide your strategy):\n"
            f"{memory_context}\n\n"
        )

    text_input = (
        f"{memory_block}"
        f"Lead's opening message: {lead_intro}\n\n"
        f"Conversation so far:\n{json.dumps(conversation_history, indent=2)}\n\n"
        f"Round: {round_num}/{MAX_ROUNDS}\n\n"
        f"Cognee feature catalog:\n{_feature_catalog_text()}\n\n"
        "Choose which feature to lead with, pick a pitch angle, and craft your message."
    )
    system_prompt = (
        "You are a Cognee sales agent. Your goal is to close deals by matching the right "
        "Cognee feature to the prospect's needs.\n\n"
        "Available features: multimodal_ingestion, knowledge_structuring, access_control, "
        "retrieval, memory, feedback.\n"
        "Available pitch angles: developer_experience, compliance, roi, simplicity, research.\n\n"
        "IMPORTANT: You do NOT know who this customer is or what persona they fit. "
        "You must figure out what they need from their messages and any past learnings.\n\n"
        "Guidelines:\n"
        "- If PAST SALES LEARNINGS are provided, follow them closely — they reflect what "
        "actually worked and failed with similar customers.\n"
        "- Listen carefully to what the customer is actually asking for.\n"
        "- Pay close attention to customer hints and objections — they reveal what the "
        "customer actually needs.\n"
        "- If the customer seems skeptical or objects, try a DIFFERENT feature.\n"
        "- Be concise and specific.\n"
    )
    return await LLMGateway.acreate_structured_output(text_input, system_prompt, SalesResponse)


async def customer_hint_turn(
    conversation_history: list[dict], profile: BuyingProfile, pitched_right: bool
) -> str:
    """Generate a customer response hint. The LLM creates natural dialogue but
    does NOT decide the outcome — that's deterministic."""
    if pitched_right:
        instruction = (
            "The sales agent pitched the RIGHT feature for you. "
            "Respond positively — show interest, ask follow-up questions about "
            "implementation details or your specific use case. "
            "Do NOT reveal which feature you need by name."
        )
    else:
        instruction = (
            "The sales agent pitched the WRONG feature for you. "
            "Respond with skepticism using your objection style. "
            "Give a HINT about what you actually need — describe your PROBLEM, "
            "not the solution. Do NOT name the feature you want directly.\n"
            f"Your objection style: {profile.objection_style}\n"
            f"What you actually need relates to: {profile.hint_theme}"
        )

    text_input = (
        f"Conversation so far:\n{json.dumps(conversation_history, indent=2)}\n\n"
        f"{instruction}"
    )
    system_prompt = (
        f"You are a customer at: {profile.company_context}.\n"
        f"Your communication style: {profile.objection_style}.\n"
        "Respond naturally in 1-3 sentences. Do NOT mention specific Cognee feature names. "
        "Describe your needs in terms of business problems, not technical solutions."
    )
    result = await LLMGateway.acreate_structured_output(
        text_input, system_prompt, CustomerHint
    )
    return result.message


def _evaluate_round(
    profile: BuyingProfile,
    sales_resp: SalesResponse,
    round_num: int,
    rounds_with_right_feature: int,
) -> tuple[str, str]:
    """Deterministic outcome evaluation. Returns (outcome, sentiment).

    Rules:
    - Right feature on R1 → CLOSED_WON (feature match is enough)
    - Right feature on R2+ → CLOSED_WON (recovery possible)
    - Wrong feature → CONTINUE/skeptical
    - Last round without right feature → CLOSED_LOST
    """
    right_feature = sales_resp.lead_feature == profile.must_have_feature

    if right_feature:
        return "CLOSED_WON", "ready_to_buy"

    # Wrong feature
    if round_num == MAX_ROUNDS:
        return "CLOSED_LOST", "skeptical"
    return "CONTINUE", "skeptical"


async def run_conversation(
    sales_fn: SalesFn,
    profile: BuyingProfile,
    memory_context: str = "",
) -> ConversationResult:
    """Run one full lead conversation with deterministic evaluation."""
    conversation_history: list[dict] = []
    features_pitched: list[str] = []
    winning_feature = None
    winning_angle = None
    rounds_with_right_feature = 0

    for round_num in range(1, MAX_ROUNDS + 1):
        # Sales turn
        sales_resp = await sales_fn(
            conversation_history, profile.initial_message, round_num, memory_context
        )
        features_pitched.append(sales_resp.lead_feature)
        conversation_history.append({
            "role": "sales",
            "message": sales_resp.message_to_customer,
            "feature": sales_resp.lead_feature,
        })

        # Track right feature count
        pitched_right = sales_resp.lead_feature == profile.must_have_feature
        if pitched_right:
            rounds_with_right_feature += 1

        # Deterministic evaluation
        outcome, sentiment = _evaluate_round(
            profile, sales_resp, round_num, rounds_with_right_feature
        )

        # Customer hint (LLM generates natural response text)
        customer_message = await customer_hint_turn(
            conversation_history, profile, pitched_right
        )
        conversation_history.append({"role": "customer", "message": customer_message})

        print(
            f"  [{profile.lead_id}] R{round_num} "
            f"feature={sales_resp.lead_feature} angle={sales_resp.pitch_angle} "
            f"sentiment={sentiment} outcome={outcome}"
        )

        if outcome in ("CLOSED_WON", "CLOSED_LOST"):
            if outcome == "CLOSED_WON":
                winning_feature = sales_resp.lead_feature
                winning_angle = sales_resp.pitch_angle
            return ConversationResult(
                lead_id=profile.lead_id,
                persona_tag=profile.persona_tag,
                outcome=outcome,
                rounds=round_num,
                features_pitched=features_pitched,
                winning_feature=winning_feature,
                winning_angle=winning_angle,
                conversation_history=conversation_history,
            )

    # Should not reach here (last round always resolves) but just in case
    return ConversationResult(
        lead_id=profile.lead_id,
        persona_tag=profile.persona_tag,
        outcome="CLOSED_LOST",
        rounds=MAX_ROUNDS,
        features_pitched=features_pitched,
        conversation_history=conversation_history,
    )


def format_trace_summary(profile: BuyingProfile, result: ConversationResult) -> str:
    """Create a concise, searchable text summary of a completed conversation."""
    features_tried = ", ".join(result.features_pitched)
    lines = [
        f"Sales conversation with a customer.",
        f"Customer said: {profile.initial_message}",
        f"Features pitched: {features_tried}.",
        f"Outcome: {result.outcome} after {result.rounds} rounds.",
    ]
    if result.outcome == "CLOSED_WON":
        lines.append(
            f"Winning approach: feature={result.winning_feature}, angle={result.winning_angle}."
        )
    else:
        lines.append("Deal lost — the pitched features or angles did not match customer needs.")
    return " ".join(lines)


async def setup_runtime() -> None:
    """Prune all data and reinitialize Cognee."""
    await cognee.prune.prune_data()
    await cognee.prune.prune_system(metadata=True)
    await setup()
